package com.woniu.po;

public class PageBean {
	
	private Integer pageNum;
	private Integer pageRow;
	private Integer countRow;
	private Integer countPage;
	/**
	 * @return the pageNum
	 */
	public Integer getPageNum() {
		return pageNum;
	}
	/**
	 * @param pageNum the pageNum to set
	 */
	public void setPageNum(Integer pageNum) {
		this.pageNum = pageNum;
	}
	/**
	 * @return the pageRow
	 */
	public Integer getPageRow() {
		return pageRow;
	}
	/**
	 * @param pageRow the pageRow to set
	 */
	public void setPageRow(Integer pageRow) {
		this.pageRow = pageRow;
	}
	/**
	 * @return the countRow
	 */
	public Integer getCountRow() {
		return countRow;
	}
	/**
	 * @param countRow the countRow to set
	 */
	public void setCountRow(Integer countRow) {
		this.countRow = countRow;
	}
	/**
	 * @return the countPage
	 */
	public Integer getCountPage() {
		return countPage;
	}
	/**
	 * @param countPage the countPage to set
	 */
	public void setCountPage(Integer countPage) {
		this.countPage = countPage;
	}
	
	
	

}
